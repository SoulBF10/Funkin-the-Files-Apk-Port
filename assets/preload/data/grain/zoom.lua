function onBeatHit()
if curBeat == 248 then
      setProperty('defaultCamZoom', 0.45);
   end
if curBeat == 252 then
      setProperty('defaultCamZoom', 0.5);
   end
if curBeat == 253 then
      setProperty('defaultCamZoom', 0.55);
   end
if curBeat == 254 then
      setProperty('defaultCamZoom', 0.6);
   end
if curBeat == 255 then
      setProperty('defaultCamZoom', 0.65);
   end
if curBeat == 256 then
      setProperty('defaultCamZoom', 0.7);
   end
end
 